      REAL*8 FUNCTION X02AAE(X)
C     MARK 4.5 RELEASE. NAG COPYRIGHT 1975
      REAL*8 X
C     * EPS *
C     RETURNS THE VALUE EPS WHERE EPS IS THE SMALLEST
C     POSITIVE
C     NUMBER SUCH THAT 1.0 + EPS > 1.0
C     THE X PARAMETER IS NOT USED
C     FOR ICL 1900
C     X02AAE = 2.0**(-37.0)
C     FOR HP-1000 (KOLPAKOW I.V.)
C     X02AAE = 2.0**(-23.0)
C     X02AAE = 1.192093D-07
C     FOR IBM-PC WITH 8087
      X02AAE = 9.536743D-07
      RETURN
      END
